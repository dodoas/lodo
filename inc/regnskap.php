<h2>Hva er et regnskap?</h2>
<p>Et regnskap er et system for registrering, vurdering og 
kommunikasjon av økonomisk informasjon. I praksis betyr dette
at man lager en redegjørelse for inntekter og kostnader i 
en gitt tidsperiode med utfyllende og godkjent dokumentasjon (f. eks. 
kvitteringer, fakturaer, kontoutskrifter fra banken, o.l.).</p>
<p>Formålet med regnskapsføringen er å registrere alle økonomiske 
transaksjoner innenfor en bestemt periode.</p>
