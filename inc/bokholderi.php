<p>Man oppretter en konto for hver post i regnskapet. En konto er en 
kasse, bank, salg, varekjøp, reiseutgifter, etc. og den viser detaljert 
informasjon om transaksjonene for en bestemt post i regnskapet (f.eks. 
en eiendel, gjeld, egenkapital, eller resultatpost).</p>

<p>Hver transaksjon som føres inn i regnskapet, kalles en <strong>postering</strong>. 
Hver postering nummereres fortløpende i regnskapet. Posteringene baseres på et papir 
eller dokument som kalles <strong>bilag</strong>.</p>

<p>Et bilag posteres med beløpet til bilaget på en konto og tilsvarende på 
en annen konto og det vil endre saldoen på kontoen.</p>

<p>I Norge har man laget en kontoplan for bedrifter - Norsk Standard" NS 4102. 
I henhold til denne standardkontoplanen har alle konti sine spesielle tallnummer. 
Dette systeme brukes også i hele Europa.</p>

