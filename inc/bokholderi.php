<h2>Dobbelt bokholderi</h2>

<p>Når man skal føre regnskap i Norge eller i de fleste andre land, 
bruker man prinsippet for <strong>dobbelt bokholderi</strong>. Dette betyr at hver 
transaksjon registreres på minst to steder (konti), med like store 
beløp til debet + og til kredit -. Sum av et bilag (dvs. debet + og kredit -) 
er alltid null i bokføring.</p>

<p>Begrepene <em>debet</em> og <em>kredit</em> som brukes i dag, har sin opprinnelse fra den
italienske renessansen. Den gangen var forretningsspåket latin. <em>Debet</em> betyr "å eie" og 
<em>kredit</em> betyr "i tillit til".</p>


<p>En konto spesifiseres slik:</p>

