<h2>Lodo blir open source</h2>

<p><b>La oss jobbe sammen for &aring; gj&oslash;re Lodo enda bedre. Vi gj&oslash;r n&aring; kildekoden til lodo tilgjengelig for de som er interessert i &aring; videreutvikle og integrere pakken med egne l&oslash;sninger. Release dato er 24.04.2006 og nedlasting blir mulig fra denne siden.</b></p>

<p> Som lisens har vi bestemt oss for en <a href="http://www.opensource.org/licenses/mit-license.php" target="_blank">MIT lisens</a>.</p>

<p>Lodo er ikke laget som et frittst&aring;ende program, men forutsetter en egen server, driftspersonell med database og webutviklingskompetanse, samt programmerere for feilretting og vedlikehold.</p>

<p>Vi tilbyr ingen support, har ikke noe ansvar for 
oppdateringer av noe slag, og gir ingen form for garantier. Vi benytter dette produktet selv, og vil derfor i egen interresse gj&oslash;re det best mulig. Nye versjoner og forbedringer vil bli lagt ut.</p>

<p>De aller, aller fleste vil nok v&aelig;re best tjent med &aring; <a href="lodo.php?page=register">leie plass p&aring; lodoserveren</a>, men om dere har ressursene til &aring; jobbe med lodo, inviterer vi dere gjerne til &aring; samarbeide om &aring; gj&oslash;re regnskapspakken enda bedre.</p>

<p>For mer informasjon, ta kontakt pr. epost p&aring; <a href="mailto:support@lodo.no">support@lodo.no</a>.
<br>Relevant teknologi: PHP5, MySQL 4.1, Apache webserver.</p>

